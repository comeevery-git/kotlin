fun main() {

    var num1:Int = 10
    var num2:Int = 20
    var result = num1 % num2
    println(result)

    result = result + 10
    println(result)

    result += 10
    println(result)

    var res:IntRange = 10..20
    print("res : $res")

    // IntRange type
    //  10..20 : 10부터 20까지의 값을 관리함

    val r1:Boolean = 10 == 10   // true
    val r2:Boolean = 10 != 10   // false

}
fun main() {
    // literal data
    // literal
    // 정수 리터럴
    println(202020202020)
    println(100)
    println(123456890)
    println(123_456_890)

    // 실수 리터럴
    println(22.33)
    println(55.77F)

    // 문자 리터럴
    println("A")
    println("B")

    // boolean : true, false
    // 논리 리터럴
    println(true)
    println(false)

    // Raw String 여러 줄 문자열

    // Noting : 함수가 비정상적인 종료를 할 때

    // Unit : void 를 의미

    // |n : next line
    println("Life is too short You need Kotlin")
    println("Life is \n too short You need Kotlin")

    println("""LIFE IS
| TOOOOO
|  SHORT""".trimMargin())

}